var a = Number(prompt("First number"));
var b = Number(prompt("Second number"));
var c = prompt("Type Operation (+,-,*,/,%)");
if(c === "+"){
    alert(a+b);
}
else if(c === "-"){
    alert(a-b);
}
else if(c === "*"){
    alert(a*b);
}
else if(c === "/"){
    alert(a/b);
}
else if(c === "%"){
    alert(a%b);
}